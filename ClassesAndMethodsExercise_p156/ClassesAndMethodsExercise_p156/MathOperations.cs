using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ClassesAndMethodsExercise_p156
{
    public class MathOperations
    {
        public static int MultiplyByThree(int number)
        {
            return number * 3;
        }

        public static int SubtractFifty(int number)
        {
            return number - 50;
        }

        public static int AddTenThousand(int number)
        {
            return number + 10000;
        }
    }
}
